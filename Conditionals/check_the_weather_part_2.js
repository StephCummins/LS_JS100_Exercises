let weather = "sunny";

switch (weather) {
  case "sunny":
    console.log("It's a beautiful day!");
    break;
  case "rainy": 
    console.log("Grab your umbrella.");
    break;
  case "cold":
    console.log("Don't forget your jacket.")
    break;
  default:
    console.log("Let's stay inside.")
    break;
}